/**
* @brief GPIO ISR callback,deal button press isr
* @param None
* @retval None
*/
static uint8_t sg_button_press_count = 0;
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
    if (GPIO_Pin == B1_Pin) {
        sg_button_press_count++;
        HAL_GPIO_TogglePin(LD2_GPIO_Port, LD2_Pin);
    }
}


/**
* @brief Get button press count
* @param None
* @retval button press count
*/
uint8_t GetButtonPressedCount(void)
{
    return sg_button_press_count;
}

/**
* @brief Clear button press count
* @param None
* @retval None
*/
void ClearButtonPressedCount(void)
{
    sg_button_press_count = 0;
}


/**
* @brief control ld2 on/off
* @param state true means on and false means off
* @retval None
*/
void Led2Ctrl(eLd2StateType type)
{
    switch (type) {
        case eLD2_OFF:
            HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_RESET);
            break;

        case eLD2_ON:
            HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_SET);
            break;

        default:
            break;
    }
}

/**
* @brief Get led2 state on/off
* @param state true means on and false means off
* @retval None
*/

eLd2StateType GetLed2State(void)
{
    eLd2StateType ret;

    if (GPIO_PIN_RESET == HAL_GPIO_ReadPin(LD2_GPIO_Port, LD2_Pin)) {
        ret = eLD2_OFF;
    } else {
        ret = eLD2_ON;
    }

    return ret;
}


